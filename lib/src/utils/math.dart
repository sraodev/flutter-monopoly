class Math {
  static double add(double val1, val2) {
    return val1 + val2;
  }

  static double subtract(double val1, val2) {
    return val1 - val2;
  }

  static double multiply(double val1, double val2) {
    return val1 * val2;
  }

  static double divide(double val1, double val2) {
    return val1 / val2;
  }
}